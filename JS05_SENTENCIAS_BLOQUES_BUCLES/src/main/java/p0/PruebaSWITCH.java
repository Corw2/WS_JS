package p0;


public class PruebaSWITCH {

	public static void main(String[] args) {
		
		
		final int valor = 4;
		
		
		switch(valor) {
		
			default:
				System.out.println("BEBIDA DESCONOCIDA");				
		
		
			case 1:
				System.out.println("WHISKY");
				

				
				
			case 2:
				System.out.println("RON");

				
		
			case 3:
				System.out.println("GINEBRA");

		}		
		
		
		
		
		
	}
	
}
